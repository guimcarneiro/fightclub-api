package com.guimcarneiro.fightclub.api.model;

public class CreateWrestlerSkillModel {

	private Long id;
	
	private Integer agility;
	
	private Integer force;
	
	private Integer nervousness;
	
	private Integer technique;
	
	private Integer endurance;
	
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Integer getAgility() {
		return agility;
	}

	public void setAgility(Integer agility) {
		this.agility = agility;
	}

	public Integer getForce() {
		return force;
	}

	public void setForce(Integer force) {
		this.force = force;
	}

	public Integer getNervousness() {
		return nervousness;
	}

	public void setNervousness(Integer nervousness) {
		this.nervousness = nervousness;
	}

	public Integer getTechnique() {
		return technique;
	}

	public void setTechnique(Integer technique) {
		this.technique = technique;
	}

	public Integer getEndurance() {
		return endurance;
	}

	public void setEndurance(Integer endurance) {
		this.endurance = endurance;
	}
	
	
	
}
